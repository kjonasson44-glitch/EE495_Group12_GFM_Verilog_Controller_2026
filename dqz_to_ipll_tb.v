// 3. Address Counter / Sequencer
// ... (Keep your existing parameters)
 // Add these new timing parameters:
 localparam CLK_FREQ_HZ     = 25_000_000;  // 25 MHz System Clock
 localparam SAMPLE_RATE_HZ  = 720;         // Target Data Rate
 localparam CLKS_PER_SAMPLE = CLK_FREQ_HZ / SAMPLE_RATE_HZ; // ~34,722 clocks

 integer timer_cnt;

 // 3. Address Counter / Sequencer (Modified for 720 Hz rate)
 // ALSO - HERE WE MAKE OUR 720 CLK ENABLE CALLED CLK 720
always @(posedge clk or posedge reset) begin
    if (reset) begin
      addr_cnt  <= 11'd0;
      timer_cnt <= 0;
      clk_720 <= 1'd0;
    end else begin
      // Check if enough clock cycles have passed to jump to the next sample
      if (timer_cnt >= CLKS_PER_SAMPLE - 1) begin
            timer_cnt <= 0;               // Reset timer
            clk_720 <= 1;
            
            // Seamless wrap logic:
            if (addr_cnt >= 11'd1199) begin
                addr_cnt <= 11'd0;        // Loop back to start of cycle
            end else begin
                addr_cnt <= addr_cnt + 11'd1; 
            end
            
      end else begin
            timer_cnt <= timer_cnt + 1;
            clk_720 <= 0;
      end
    end
 end
 // ... (Rest of your module)
 
 // at least now our sig_a b c actually represent what they are actually going to look like 

// 4. Assign memory values to UUT input wires
assign sig_a   = mem_phase_a[addr_cnt];
assign sig_b   = mem_phase_b[addr_cnt];
assign sig_c   = mem_phase_c[addr_cnt];
assign sig_s   = mem_sine_ramp[addr_cnt];
assign sig_cos = mem_cosine_ramp[addr_cnt];
