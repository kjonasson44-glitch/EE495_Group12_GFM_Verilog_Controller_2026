// ===== Parameters for ipll =====
localparam WORD_SIZE = 18;        // 18-bit signed output
localparam ADDR_SIZE = 10;        // 10-bit addressing
localparam ACC_WIDTH = 32;        // 32-bit phase accumulator

// ===== Signals =====
wire  signed [WORD_SIZE-1:0] q_in;
wire signed [ACC_WIDTH-1:0] phase_out;

// ===== Clock Generation =====
initial begin
    clk = 0;
    forever begin 
        #(PERIOD/2);
        clk = ~clk;
    end
end

// ===== DUT Instantiation =====
ipll dut (
    .clk(clk),
	 .clk_en(clk_720),
    .reset(reset),
    .q_in(q_in),
    .freq_out(phase_out) //output frequency which goes into dqz
);

